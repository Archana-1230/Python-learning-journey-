# Grade Calculator
mark = int(input("Enter the mark: "))
if mark >= 90:
    print("A grade")
elif mark >= 80:
    print("B grade")
elif mark >= 70:
    print("C grade")
elif mark >= 50:
    print("Pass")
else:
    print("Fail")
